    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jTextField1.getText().isEmpty()|| jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene el campo");
        }else{
        try {
            // TODO add your handling code here:
            conexion  cc= new conexion();
             Connection cn= cc.conectar();
            Map parametros = new HashMap();
            JasperReport reporte=JasperCompileManager.compileReport("D:\\programasjava\\reportes\\dosparametros.jrxml");
            parametros.put("placa", jTextField1.getText());
             parametros.put("chofer", jTextField2.getText());
            JasperPrint impresion = JasperFillManager.fillReport(reporte, parametros,cn);
            //JasperViewer.viewReport(impresion,false);
            JRViewer vista = new JRViewer(impresion);
            JInternalFrame sj = new JInternalFrame();
            sj.setClosable(true);
            sj.add(vista);
            sj.setSize(800, 800);
            sj.setVisible(true);
            Principal.jdspPrincipal.add(sj);
      
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }}
    }//GEN-LAST:event_jButton1ActionPerformed

